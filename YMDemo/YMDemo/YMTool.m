}

#pragma mark - 微信分享
- (void)ym_WXShare{
    //弹出需要显示的分享框
    [UMSocialUIManager setPreDefinePlatforms:@[@(UMSocialPlatformType_Sina),@(UMSocialPlatformType_QQ),@(UMSocialPlatformType_WechatSession),@(UMSocialPlatformType_AlipaySession)]];
